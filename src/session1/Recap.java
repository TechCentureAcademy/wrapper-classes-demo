package session1;

public class Recap {

	public static void main(String[] args) {
		
		
		int age = 10;
		age++;
		age = 11;
		
		//Lets wrap these primitive variabels with their corresponding class types
		
		//the process of converting a primitive type to its wrapper type: auto-boxing
		Integer ageWrap = age;
		
			
		Integer number = 13;
		// we are converting a wrapper type to a primitive type:  unboxing
		int numberPrim = number;
		
		
		/*
		 * Whenever you convert:
		 * From a Primitive type to a Wrapper type => auto-boxing
		 * From a Wrapper type to a Primitive type => unboxing
		 * 
		 * 
		 * 
		 */
		
		
	}
}
